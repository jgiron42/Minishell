#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!(new))
		return (NULL);
	new->finalpos = 0;
	new->value = content;
	new->next = NULL;
	return (new);
}
